    private void salvar_tfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvar_tfActionPerformed
        String id = id_tf.getText();
        String nome = nome_tf.getText();
        String email = email_tf.getText();
        String senha = senha_tf.getText();

        modelo.addRow(new String[]{id, nome, email, senha});
        tabeladados.setModel(modelo);
        Banco Clientes = new Banco();
        Clientes.setId(id_tf.getText());
        Clientes.setNome(nome_tf.getText());
        Clientes.setEmail(email_tf.getText());
        Clientes.setSenha(senha_tf.getText());

        JOptionPane.showMessageDialog(null, Clientes.salvar());
        id_tf.setText("");
        nome_tf.setText("");
        email_tf.setText("");
        senha_tf.setText("");

    }//GEN-LAST:event_salvar_tfActionPerformed

