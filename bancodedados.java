    private void pesquisa_tfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisa_tfActionPerformed
        String pesquisa = pesquisa1_tf.getText();

        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (pesquisa.equals(tabeladados.getValueAt(i, a))) {
                id_tf.setText((String) tabeladados.getValueAt(i, a));
                nome_tf.setText((String) tabeladados.getValueAt(i, b));
                email_tf.setText((String) tabeladados.getValueAt(i, c));
                senha_tf.setText((String) tabeladados.getValueAt(i, d));
            }

        }
    }//GEN-LAST:event_pesquisa_tfActionPerformed

    private void editar_tfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editar_tfActionPerformed
        String id = id_tf.getText();
        String nome = nome_tf.getText();
        String email = email_tf.getText();
        String senha = senha_tf.getText();
        String pesquisa = pesquisa1_tf.getText();

        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (pesquisa.equals(tabeladados.getValueAt(i, a))) {
                tabeladados.setValueAt(id, i, a);
                tabeladados.setValueAt(nome, i, b);
                tabeladados.setValueAt(email, i, c);
                tabeladados.setValueAt(senha, i, d);
            }

        }
    }//GEN-LAST:event_editar_tfActionPerformed

    private void excluir_tfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluir_tfActionPerformed
        String pesquisa = pesquisa1_tf.getText();
        for (int i = 0; i < modelo.getRowCount(); i++) {
            if (pesquisa.equals(tabeladados.getValueAt(i, a))) {
                modelo.removeRow(i);
            }
        }
    }//GEN-LAST:event_excluir_tfActionPerformed

